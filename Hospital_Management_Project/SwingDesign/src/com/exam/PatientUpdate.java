    private void btnPatientaUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPatientaUpdateActionPerformed
        // TODO add your handling code here:

        try {

            if (!Patientuid.getText().equals("")) {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

                pst = con.prepareStatement("update addpatient set Name=?,Age=?,Department=?,Gender=?,MaritalStatus=?,Address=?,Date=?,Phone=? where PatientID=?");

                pst.setString(1, Patientuname.getText());
                pst.setString(2, Patientuage.getText());
                String sp = Patientudepartment.getSelectedItem().toString();
                pst.setString(3, sp);
                String gen = "";
                if (Patientuf.isSelected()) {
                    gen = Patientuf.getText();
                }
                if (Patientum.isSelected()) {
                    gen = Patientum.getText();
                }
                pst.setString(4, gen);

                String spp = Patientumaritalstatus.getSelectedItem().toString();
                pst.setString(5, spp);

                pst.setString(6, Patientuaddress.getText());

                Date dte = new Date(Patientudate.getDate().getTime());
                pst.setDate(7, dte);
                pst.setString(8, Patientuphone.getText());

                pst.setInt(9, Integer.parseInt(Patientuid.getText()));

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Update Successfully");

            } else {

                JOptionPane.showMessageDialog(null, "FillUp All Field Is Requred");
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnPatientaUpdateActionPerformed

