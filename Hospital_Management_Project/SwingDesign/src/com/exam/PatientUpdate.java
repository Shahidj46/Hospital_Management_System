    private void PatientuidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PatientuidKeyReleased
        // TODO add your handling code here:
        if (Patientuid.getText().equals("")) {
            Patientuid.setText(String.valueOf(0));
        }
        try {
            // TODO add your handling code here:

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("select * from addpatient where PatientID=?");

            pst.setInt(1, Integer.parseInt(Patientuid.getText()));

            ResultSet rst = pst.executeQuery();

            while (rst.next()) {
                String nam = rst.getString("Name");
                String agee = rst.getString("Age");
                String depatmnt = rst.getString("Department");
                String gndr = rst.getString("Gender");
                String maried = rst.getString("MaritalStatus");
                String adress = rst.getString("Address");
                dte = rst.getDate("Date");
                String phn = rst.getString("Phone");

                Patientuname.setText(nam);
                Patientuage.setText(agee);
                Patientudepartment.setSelectedItem(depatmnt);

                if (gndr.equals("Female")) {
                    Patientuf.setSelected(true);
                } else if (gndr.equals("Male")) {
                    Patientum.setSelected(true);

                }
                Patientumaritalstatus.setSelectedItem(maried);
                Patientudate.setDate(dte);

                Patientuaddress.setText(adress);
                Patientuphone.setText(phn);

            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_PatientuidKeyReleased

