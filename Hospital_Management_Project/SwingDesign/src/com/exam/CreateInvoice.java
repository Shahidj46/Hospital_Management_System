    private void InvoicePatientDiscountKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_InvoicePatientDiscountKeyReleased
        // TODO add your handling code here:
        InvoicePatientGrandTotal.setText(null);
        if (InvoicePatientDiscount.getText().equals("")) {
            InvoicePatientDiscount.setText(String.valueOf(0));
        }
        double bed = Double.parseDouble(String.valueOf(InvoicePatientBedCharge.getText()));
        double doctor = Double.parseDouble(String.valueOf(InvoicePatientDoctorCharge.getText()));
        double pathology = Double.parseDouble(String.valueOf(InvoicePatientPathologyCharge.getText()));
        double t = bed + doctor + pathology;

        double dis = Double.parseDouble(String.valueOf(InvoicePatientDiscount.getText()));
        double fdis = t * (dis / 100);
        double ftotal = t - fdis;
        InvoicePatientGrandTotal.setText(String.valueOf(String.format("%.2f", ftotal)));


    }//GEN-LAST:event_InvoicePatientDiscountKeyReleased

