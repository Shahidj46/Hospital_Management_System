    private void btnInvoiceCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInvoiceCreateActionPerformed
        // TODO add your handling code here:

        try {

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("insert into invoice(InvoiceID,PatientID,Name,Phone,Date,BedCharge,DoctorCharge,PathologyCharge,Total,Discount,GrandTotal) values(?,?,?,?,?,?,?,?,?,?,?)");

            if (!InvoicePatientid.getText().equals("") || InvoicePatientid.getText().equals(0)) {
                pst.setInt(1, Integer.parseInt(Invoiceid.getText()));
                pst.setString(2, InvoicePatientid.getText());
                pst.setString(3, InvoicePatientName.getText());
                pst.setString(4, InvoicePatientPhone.getText());

                Date dte = new Date(InvoicePatientDate.getDate().getTime());

                pst.setDate(5, dte);

                pst.setString(6, InvoicePatientBedCharge.getText());
                pst.setString(7, InvoicePatientDoctorCharge.getText());
                pst.setString(8, InvoicePatientPathologyCharge.getText());
                pst.setString(9, InvoicePatientTotal.getText());
                pst.setString(10, InvoicePatientDiscount.getText());
                pst.setString(11, InvoicePatientGrandTotal.getText());

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Insert Succesfully");

            } else {
                JOptionPane.showMessageDialog(null, "Fill-Up All Field Is Requred");
            }

        } catch (java.sql.SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Duplicate entry!");
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnInvoiceCreateActionPerformed

