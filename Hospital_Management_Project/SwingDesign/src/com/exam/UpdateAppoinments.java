    private void AppoinmendiduKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_AppoinmendiduKeyReleased
        if (Appoinmendidu.getText().equals("")) {
            Appoinmendidu.setText(String.valueOf(0));
        }
        try {
            // TODO add your handling code here:

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("select * from appoinment where AppoinmentID=?");

            pst.setInt(1, Integer.parseInt(Appoinmendidu.getText()));

            ResultSet rst = pst.executeQuery();

            while (rst.next()) {
                String doctrid = rst.getString("DoctorID");
                String patintid = rst.getString("PatientID");
                String doctrname = rst.getString("DoctorName");
                String patintname = rst.getString("PatientName");
                String doctrdeprtmnt = rst.getString("DoctorDepartment");
                String Patintaddrss = rst.getString("PatientAddress");
                String doctremail = rst.getString("DoctorEmail");
                String patintphon = rst.getString("PatientPhone");
                dte = rst.getDate("Date");
                String tim = rst.getString("Time");
                
                AppoinmentDoctoridDeleteu.setText(doctrid);
                AppoinmentPatientidDeleteu.setText(patintid);
                AppoinmentDoctorNameDeleteu.setText(doctrname);
                AppoinmentPatientNameDeleteu.setText(patintname);
                AppoinmentDoctorDepartmentDeleteu.setText(doctrdeprtmnt);
                AppoinmentPatientAddressDeleteu.setText(Patintaddrss);
                AppoinmentDoctorEmailDeleteu.setText(doctremail);
                AppoinmentPatientPhoneDeleteu.setText(patintphon);
                AppoinmentDateDeleteu.setDate(dte);
                AppoinmentTimeDeleteu.setText(tim);
                

            }

        } catch (SQLException e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_AppoinmendiduKeyReleased

