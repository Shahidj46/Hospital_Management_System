    private void btnAppoinmentuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAppoinmentuActionPerformed
        // TODO add your handling code here:

        try {

            if (!AppoinmentDoctoridDeleteu.getText().equals("")) {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

                pst = con.prepareStatement("update appoinment set DoctorID=?,PatientID=?,DoctorName=?,PatientName=?,DoctorDepartment=?,PatientAddress=?,DoctorEmail=?,PatientPhone=?,Date=?,Time=? where AppoinmentID=?");

                pst.setString(1, AppoinmentDoctoridDeleteu.getText());
                pst.setString(2, AppoinmentPatientidDeleteu.getText());
                pst.setString(3, AppoinmentDoctorNameDeleteu.getText());
                pst.setString(4, AppoinmentPatientNameDeleteu.getText());
                pst.setString(5, AppoinmentDoctorDepartmentDeleteu.getText());
                pst.setString(6, AppoinmentPatientAddressDeleteu.getText());
                pst.setString(7, AppoinmentDoctorEmailDeleteu.getText());
                pst.setString(8, AppoinmentPatientPhoneDeleteu.getText());
                Date dte = new Date(AppoinmentDateDeleteu.getDate().getTime());
                pst.setDate(9, dte);
                pst.setString(10, AppoinmentTimeDeleteu.getText());

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Update Successfully");

            } else {

                JOptionPane.showMessageDialog(null, "FillUp All Field Is Requred");
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnAppoinmentuActionPerformed

