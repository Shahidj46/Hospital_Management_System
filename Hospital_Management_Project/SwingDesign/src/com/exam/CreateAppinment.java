    private void btnAppoinmentCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAppoinmentCreateActionPerformed
        // TODO add your handling code here:

        try {

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("insert into appoinment(AppoinmentID,DoctorID,PatientID,DoctorName,PatientName,DoctorDepartment,PatientAddress,DoctorEmail,PatientPhone,Date,Time) values(?,?,?,?,?,?,?,?,?,?,?)");

            if (!AppoinmentDoctorid.getText().equals("") || AppoinmentDoctorid.getText().equals(0)) {
                pst.setInt(1, Integer.parseInt(Appoinmentid.getText()));
                pst.setString(2, AppoinmentDoctorid.getText());
                pst.setString(3, AppoinmentPatientid.getText());
                pst.setString(4, AppoinmentDoctorName.getText());
                pst.setString(5, AppoinmentPatientName.getText());
                pst.setString(6, AppoinmentDoctorDepartment.getText());
                pst.setString(7, AppoinmentPatientAddress.getText());
                pst.setString(8, AppoinmentDoctorEmail.getText());
                pst.setString(9, AppoinmentPatientPhone.getText());
                dte = new Date(AppoinmentDate.getDate().getTime());
                pst.setDate(10, dte);
                pst.setString(11, AppoinmentTime.getText());
                


                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Insert Succesfully");

            } else {
                JOptionPane.showMessageDialog(null, "Fill-Up All Field Is Requred");
            }

        } catch (java.sql.SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Duplicate entry!");
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnAppoinmentCreateActionPerformed

