    public void lastidd() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");
            String sql = "select max(AppoinmentID)from appoinment";
            st = con.createStatement();
            rs = st.executeQuery(sql);

            if (rs.next()) {
                lastid = rs.getInt(1);
                lastid++;
                Appoinmentid.setText(Integer.toString(lastid));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void getPatientinfo() {
        if (AppoinmentDoctorid.getText().equals("")) {
            AppoinmentDoctorid.setText(String.valueOf(0));
        }
        try {
            // TODO add your handling code here:

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("select * from adddoctor where DoctorID=?");

            pst.setInt(1, Integer.parseInt(AppoinmentDoctorid.getText()));

            ResultSet rst = pst.executeQuery();

            while (rst.next()) {
                String nam = rst.getString("Name");
                String depatmnt = rst.getString("Department");
                String email = rst.getString("Email");
             
            

                AppoinmentDoctorName.setText(nam);
                AppoinmentDoctorDepartment.setText(depatmnt.toString());
                AppoinmentDoctorEmail.setText(email);

            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
