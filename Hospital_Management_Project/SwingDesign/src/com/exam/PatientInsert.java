    private void btnPatientaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPatientaddActionPerformed
        // TODO add your handling code here:
        try {

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("insert into addpatient(PatientID,Name,Age,Department,Gender,MaritalStatus,Address,Date,Phone) values(?,?,?,?,?,?,?,?,?)");

            if (!Patientaname.getText().equals("")) {
                pst.setInt(1, Integer.parseInt(Patientaid.getText()));
                pst.setString(2, Patientaname.getText());
                pst.setString(3, Patientaage.getText());
                String sp = Patientadepartment.getSelectedItem().toString();
                pst.setString(4, sp);
                String gen = "";
                if (Patientaf.isSelected()) {
                    gen = Patientaf.getText();
                }
                if (Patientam.isSelected()) {
                    gen = Patientam.getText();
                }
                pst.setString(5, gen);
                
                 String spp = Patientamaritalstatus.getSelectedItem().toString();
                pst.setString(6, spp);
              
                pst.setString(7, Patientaaddress.getText());

                Date dte = new Date(Patientadate.getDate().getTime());
                 pst.setDate(8, dte);
                  pst.setString(9, Patientaphone.getText());

               


                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Insert Succesfully");
               
            } else {
                JOptionPane.showMessageDialog(null, "Fill-Up All Field Is Requred");
            }
       
        } catch (java.sql.SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Duplicate entry!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnPatientaddActionPerformed

