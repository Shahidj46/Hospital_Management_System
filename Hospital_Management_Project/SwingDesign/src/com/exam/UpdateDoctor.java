    private void DoctoruidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DoctoruidKeyReleased
        // TODO add your handling code here:
        if (Doctoruid.getText().equals("")) {
            Doctoruid.setText(String.valueOf(0));
        }
        try {
            // TODO add your handling code here:

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

            pst = con.prepareStatement("select * from adddoctor where DoctorID=?");

            pst.setInt(1, Integer.parseInt(Doctoruid.getText()));

            ResultSet rst = pst.executeQuery();

            while (rst.next()) {
                String nam = rst.getString("Name");
                String depatmnt = rst.getString("Department");
                String gndr = rst.getString("Gender");
                String emaill = rst.getString("Email");
                String adress = rst.getString("Address");
                dte = rst.getDate("JoiningDate");
                dtee = rst.getDate("LeavingDate");

                Doctoruname.setText(nam);
                Doctorudepartment.setSelectedItem(depatmnt);
                
                if(gndr.equals("Female")){
                    Doctoruf.setSelected(true);
                }else if(gndr.equals("Male")){
                     Doctorum.setSelected(true);
                }
                
                Doctoruemail.setText(emaill);
                Doctoruaddress.setText(adress);

                Doctorujoin.setDate(dte);
                Doctoruleav.setDate(dtee);

            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_DoctoruidKeyReleased

