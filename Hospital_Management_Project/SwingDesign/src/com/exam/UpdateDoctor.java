    private void btndoctorUptadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndoctorUptadeActionPerformed
        // TODO add your handling code here:
        try {

            if (!Doctoruid.getText().equals("")) {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalbd", "root", "nh123456");

                pst = con.prepareStatement("update adddoctor set Name=?,Department=?,Gender=?,Email=?,Address=?,JoiningDate=?,LeavingDate=? where DoctorID=?");

              
                pst.setString(1, Doctoruname.getText());
                String sp = Doctorudepartment.getSelectedItem().toString();
                pst.setString(2, sp);
                  String gen = "";
                if (Doctoruf.isSelected()) {
                    gen = Doctoruf.getText();
                }
                if (Doctorum.isSelected()) {
                    gen = Doctorum.getText();
                }
                pst.setString(3, gen);

                pst.setString(4, Doctoruemail.getText());
                pst.setString(5, Doctoruaddress.getText());

                Date dte = new Date(Doctorujoin.getDate().getTime());
                pst.setDate(6, dte);

                Date dtee = new Date(Doctoruleav.getDate().getTime());
                pst.setDate(7, dtee);
                
                 pst.setInt(8, Integer.parseInt(Doctoruid.getText()));

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Update Successfully");

            } else {

                JOptionPane.showMessageDialog(null, "FillUp All Field Is Requred");
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btndoctorUptadeActionPerformed

